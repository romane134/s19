#include "libft.h"

void	ft_error(const char *msg)
{
	write(2, "\033[31;1m", 7);
	write(2, "(ERREUR) ", 9);
	write(2, msg, ft_strlen(msg));
	write(2, "\n", 1);
	write(2, "\033[0m", 4);
}
