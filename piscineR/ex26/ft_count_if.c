int		ft_count_if(char **tab, int (*f)(char*))
{
	int a;
	int number;

	number = 0;
	a = 0;
	while (tab[a] != 0)
	{
		if (f(tab[a]) == 1)
			number++;
		a++;
	}
	return (number);
}
