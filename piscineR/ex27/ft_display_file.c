#include <fcntl.h>
#include <unistd.h>
#include <stdio.h>
#define BUF_SIZE 1

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		ft_putchar(str[a]);
		a++;
	}
}

int		main(int argc, char **argv)
{
	int		fd;
	char	buf[BUF_SIZE + 1];

	if (argc == 1)
		write(1, "File name missing.\n", 19);
	else if (argc > 2)
		write(1, "Too many arguments.\n", 20);
	else
	{
		fd = open(argv[1], O_RDONLY);
		buf[1] = 0;
		while (read(fd, buf, BUF_SIZE))
		{
			printf("%s\n", buf);
			//ft_putstr(buf);
		}
	}
	return (0);
}
