#include <unistd.h>

int		main(int argc, char **argv)
{
	int a;
	int b;

	a = argc - 1;
	b = 0;
	if (argc > 1)
	{
		while (argv[a][b])
		{
			write (1, &argv[a][b], 1);
			b++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
